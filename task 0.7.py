######celsius function#####
def tempcelsius(fahrenheit):
 celsius=(fahrenheit-32)*5/9
 print("Temperature in Celsius :",round(celsius,0))
tempcelsius(79)

######fahrenheit function#####
def tempfahrenheit(celsius):
 fahrenheit =(celsius*9/5)+ 32
 print("Temperature in Fahrenheit :",round(fahrenheit,0))
tempfahrenheit(26)

